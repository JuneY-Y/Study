Problem Description (Q7)

Write a POSIX-compatible shell script that takes, as command-line arguments, an unordered list of positive integers ranging from n to m, with possibly one missing integer.

Your task:
	•	Find and print the missing integer between n and m, if there is one.
	•	The missing number will never be n or m, and only one integer at most will be missing.
	•	There are no duplicate integers.
	•	Input will contain only digits, passed as command-line arguments.